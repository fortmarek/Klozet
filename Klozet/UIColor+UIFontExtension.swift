import UIKit 

extension UIColor {
    class var mainOrange: UIColor {
        return UIColor(red: 1.00, green: 0.42, blue: 0.20, alpha: 1.0)
    }
    
    class var lightOrange: UIColor {
        return UIColor(red: 255.0 / 255.0, green: 108.0 / 255.0, blue: 50.0 / 255.0, alpha: 1.0)
    }
}
